pi = 3.141592
raio = float(input().strip())
circunferencia = 2 * pi * raio
Acirculo = pi * raio ** 2
Aesfera = 4 * pi * raio ** 2
Vesfera = 4 / 3 * pi * raio ** 3
print ('{:.6f}'.format(circunferencia))
print ('{:.6f}'.format(Acirculo))
print ('{:.6f}'.format(Aesfera))
print ('{:.6f}'.format(Vesfera))